#!/usr/bin/env node
/** Regenerate extractors/registry.ts from all extractor modules on disk. */
import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const EXTRACTORS_DIR = join(dirname(fileURLToPath(import.meta.url)), "../src/streamflix/extractors");

const SKIP = new Set([
  "registry.ts",
  "types.ts",
  "voe-decrypt.ts",
]);

const IMPLEMENTED_PRIORITY = [
  "vixcloud.ts",
  "rabbitstream.ts",
  "vidplay.ts",
  "streamtape.ts",
  "voe.ts",
  "filemoon.ts",
  "mixdrop.ts",
  "loadx.ts",
  "dood-la.ts",
  "stream-wish.ts",
  "vid-guard.ts",
  "two-embed.ts",
];

function toExportName(file) {
  const base = file.replace(/\.ts$/, "");
  const parts = base.split("-");
  return parts[0] + parts.slice(1).map((p) => p[0].toUpperCase() + p.slice(1)).join("") + "Extractor";
}

const files = readdirSync(EXTRACTORS_DIR)
  .filter((f) => f.endsWith(".ts") && !SKIP.has(f) && !f.startsWith("."))
  .filter((f) => !f.includes("/"));

const ordered = [
  ...IMPLEMENTED_PRIORITY.filter((f) => files.includes(f)),
  ...files.filter((f) => !IMPLEMENTED_PRIORITY.includes(f)).sort(),
];

const imports = ordered
  .map((f) => `import { ${toExportName(f)} } from "./${f.replace(/\.ts$/, "")}.js";`)
  .join("\n");

const list = ordered.map((f) => `  ${toExportName(f)},`).join("\n");

const content = `/** AUTO-GENERATED by scripts/regenerate-extractor-registry.mjs — do not edit */
import type { ExtractedVideo, ExtractorDef } from "../types.js";
import { fetchText } from "../http.js";
import { BROWSER_UA } from "../http.js";
${imports}

const EXTRACTORS: ExtractorDef[] = [
${list}
];

const URL_PREFIX_RE = /^(https?:\\/\\/)?(www\\.)?/i;

function normalizeCompareUrl(link: string): string {
  return link.toLowerCase().replace(URL_PREFIX_RE, "");
}

function stripDomain(url: string): string {
  return url.replace(/^(https?:\\/\\/)?(www\\.)?(.*?)(\\.[a-z]+)/i, "$3");
}

async function resolveBridge(link: string): Promise<string> {
  if (!link.includes("mysync.mov/stream/")) return link;
  try {
    const html = await fetchText(link, { headers: { "User-Agent": BROWSER_UA } });
    for (const re of [/window\\.location\\.replace\\("([^"]+)"/, /src="(https?:\\/\\/[^"]+)"/]) {
      const m = html.match(re);
      if (m?.[1]?.startsWith("http")) return m[1];
    }
  } catch { /* fall through */ }
  return link;
}

export async function extractVideo(link: string, serverName?: string): Promise<ExtractedVideo> {
  let finalLink = await resolveBridge(link);
  const compareUrl = normalizeCompareUrl(finalLink);
  let found: ExtractorDef | null = null;

  for (const ext of EXTRACTORS) {
    if (compareUrl.startsWith(normalizeCompareUrl(ext.mainUrl))) { found = ext; break; }
    for (const alias of ext.aliasUrls ?? []) {
      if (compareUrl.startsWith(normalizeCompareUrl(alias))) { found = ext; break; }
    }
    if (found) break;
  }

  if (!found) {
    for (const ext of EXTRACTORS) {
      if (compareUrl.startsWith(stripDomain(ext.mainUrl).toLowerCase())) { found = ext; break; }
      for (const alias of ext.aliasUrls ?? []) {
        if (compareUrl.startsWith(stripDomain(alias).toLowerCase())) { found = ext; break; }
      }
      if (found) break;
    }
  }

  if (!found && serverName) {
    found = EXTRACTORS.find((e) => serverName.toLowerCase().includes(e.name.toLowerCase())) ?? null;
  }

  if (!found) throw new Error(\`No extractor found for URL: \${finalLink}\`);
  return found.extract(finalLink, serverName);
}

export function listExtractors(): string[] {
  return EXTRACTORS.map((e) => e.name);
}

export function getExtractorRegistry(): ExtractorDef[] {
  return EXTRACTORS;
}
`;

writeFileSync(join(EXTRACTORS_DIR, "registry.ts"), content);
console.log(`Registry: ${ordered.length} extractors`);
